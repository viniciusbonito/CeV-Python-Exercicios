# Crie um programa que peça ao usuário que insira um número e retorne o antecessor e o sucessor

numero = int(input('Digite um número para saber seu antecessor e seu sucessor: '))
antecessor = numero - 1 
sucessor = numero + 1 

print(f'Analisando o número {numero} seu antecessor é {antecessor} e seu sucessor é {sucessor}')
